export type ElementStatus = "active" | "locked";

export type ElementRecord = {
  number: number;
  symbol: string;
  name: string;
  mass: string;
  period: number;
  group: number;
  tableRow: number;
  tableColumn: number;
  series?: "lanthanide" | "actinide";
  status: ElementStatus;
  slug: string;
};

type ElementSeed = Omit<ElementRecord, "status" | "slug">;

const main = (
  number: number,
  symbol: string,
  name: string,
  mass: string,
  period: number,
  group: number,
): ElementSeed => ({
  number,
  symbol,
  name,
  mass,
  period,
  group,
  tableRow: period,
  tableColumn: group,
});

const fBlock = (
  number: number,
  symbol: string,
  name: string,
  mass: string,
  series: "lanthanide" | "actinide",
  offset: number,
): ElementSeed => ({
  number,
  symbol,
  name,
  mass,
  period: series === "lanthanide" ? 6 : 7,
  group: 3,
  tableRow: series === "lanthanide" ? 9 : 10,
  tableColumn: offset + 3,
  series,
});

